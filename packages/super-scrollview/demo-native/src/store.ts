const demoMessages = LoremIpsum.replace(/\r?\n/g, '')
	.split('.')
	.map(s => s.trim() + '.');

export class MessageStore {
	@observable contacts = new Array<Message>();

	constructor() {}

	public readonly ref = React.createRef<SuperScrollView<any>>();

	public readonly onLayout = new EventEmitter<{ item: Message }>();

	private createRandomMessage(): Message {
		const c = new Message();
		c.text = `${this.contacts.length}: ${
			demoMessages[this.contacts.length % demoMessages.length]
		}`;
		return c;
	}

	@action
	addMessage() {
		console.log('test');

		const c = this.createRandomMessage();
		this.contacts.splice(this.contacts.length - 1, 0, c);
		return c;
	}

	@action
	loadMessage() {
		let shouldScrollDown = true;
		if (this.ref.current) {
			//shouldScrollDown = this.ref.current.debouncedIsScrolledToBottom;
		}

		const c = this.createRandomMessage();
		this.contacts.unshift(c);

		if (this.ref.current && shouldScrollDown) {
			// ref.itemsRendered.one(i => i === last).then(() => scroll)
			let stop = false;
			const d = this.onLayout.sub(({ item }) => {
				if (item === c) {
					setTimeout(() => {
						if (stop) {
							return;
						}
						stop = true;
						this.ref.current!.scrollDown();
					}, 100);
					d.dispose();
				}
			});
			setTimeout(() => {
				if (stop) {
					return;
				}
				stop = true;
				d.dispose();
				this.ref.current!.scrollDown();
			}, 300);
		}

		//return c;
	}
}
